function toCardData(dorm) {
  return {
    id: dorm.id,
    name: dorm.name,
    type: dorm.type,
    price: dorm.price,
    status: dorm.status,
    location: dorm.location,
    block: dorm.block,
    amenities: dorm.amenities,
    image: dorm.images[0],
    images: dorm.images,
  }
}

function toBrowseData(dorm) {
  return {
    id: dorm.id,
    name: dorm.name,
    type: dorm.type,
    price: dorm.price,
    location: dorm.location,
    block: dorm.block,
    amenities: dorm.amenities,
    status: dorm.status,
    image: dorm.images[0],
    images: dorm.images,
  }
}

function toDetailsData(dorm) {
  return {
    id: dorm.id,
    name: dorm.name,
    type: dorm.type,
    status: dorm.status,
    location: dorm.location,
    rent: dorm.price,
    amenityTags: dorm.amenities,
    specs: dorm.specs,
    amenities: dorm.detailedAmenities,
    gallery: dorm.images,
  }
}

const dormDetails = dormCatalog.map(toDetailsData)

export const featuredDorms = dormCatalog.filter((dorm) => dorm.featured).map(toCardData)
export const browseDorms = dormCatalog.map(toBrowseData)
export const comparableDorms = dormCatalog.map(toDetailsData)

export function getDormDetailsById(id) {
  return dormDetails.find((dorm) => dorm.id === id) || null
}
